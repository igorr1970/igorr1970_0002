# ДЗ Строки и индексация строк
example = 'Двигатель'
print(example)
print(example[0]) # первый символ этой строки
print(example[-1]) # последний символ этой строки (используя отрицательный индекс)
print(example[4:]) # вторую половину этой строки (С нечётным количеством символов)
print(example[::-1]) #  это слово наоборот
print(example[1:8:2]) # каждый второй символ этой строки